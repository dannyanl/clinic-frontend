type Lang = keyof typeof dictionaries
type Key = keyof (typeof dictionaries)['en']

interface I18nState {
  lang: Lang
  setLang: (l: Lang) => void
  t: (k: Key, vars?: Record<string, string>) => string
}

export const SUPPORTED_LANGS: { code: Lang; label: string; flag: string }[] = [
  { code: 'en', label: 'English', flag: '🇺🇸' },
  { code: 'es', label: 'Español', flag: '🇪🇸' },
  { code: 'pt', label: 'Português', flag: '🇧🇷' },
  { code: 'fr', label: 'Français', flag: '🇫🇷' },
]

function getBrowserLang(): Lang {
  const browserLang = navigator.language?.slice(0, 2) as Lang
  const supported: Lang[] = ['en', 'es', 'pt', 'fr']
  return supported.includes(browserLang) ? browserLang : 'en'
}

export const useI18n = create<I18nState>((set, get) => ({
  lang: (localStorage.getItem('lang') as Lang) || getBrowserLang(),
  setLang: (l) => {
    localStorage.setItem('lang', l)
    document.documentElement.lang = l
    set({ lang: l })
  },
  t: (k, vars) => {
    const dict = dictionaries[get().lang] as Record<string, string>
    const fallback = (dictionaries['en'] as Record<string, string>)[k] ?? k
    let str = dict[k] ?? fallback
    if (vars) {
      for (const [key, val] of Object.entries(vars)) {
        str = str.replace(`{${key}}`, val)
      }
    }
    return str
  },
}))
